# This is a Python program to demonstrate different data types

# Literal

print("Literal:", 10)
print('Literal:', 3214732)

# Integer
y = -20
yy = -762
print("Integer:", y)
print('Integer:', yy)

# Float
z = 15.5
zz = 3.14159
print("Float:", z)
print('Float:', zz)

# String
name = "John"
last_name = "Doe"
print("String:", name)
print('String:', last_name)

# Boolean
is_active = True
is_present = False
print("Boolean:", is_active)
print('Boolean:', is_present)
